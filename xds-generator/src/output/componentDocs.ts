/**
 * Output Spec §6 — 컴포넌트 명세 MD 양식.
 *
 * Sprint 1D 범위: L1 핵심 5종 (Button · Input · Card · Modal · Form).
 * 각 명세서는 9개 섹션 중 핵심 4섹션(정의 · Anatomy · 토큰 매핑 · 접근성)을 충실히 채우고,
 * Variants·Do·Don't·코드 예시·관련 컴포넌트는 v0.2 컴포넌트 카탈로그에서 확장.
 */

import type { TokenTree } from "../tokens/types";
import { GENERATOR_VERSION } from "../version";

export interface ComponentDoc {
  /** 파일명 기준 이름 — "Button" · "Input" 등. */
  name: string;
  /** MD 본문. */
  content: string;
}

interface Ctx {
  tree: TokenTree;
  projectName: string;
  projectVersion: string;
}

export function generateComponentDocs(ctx: Ctx): ComponentDoc[] {
  return [
    { name: "Button", content: buttonDoc(ctx) },
    { name: "Input", content: inputDoc(ctx) },
    { name: "Card", content: cardDoc(ctx) },
    { name: "Modal", content: modalDoc(ctx) },
    { name: "Form", content: formDoc(ctx) },
  ];
}

function header(component: string, ctx: Ctx): string {
  return [
    `# ${component}`,
    "",
    `> Generated by XDS Generator v${GENERATOR_VERSION} | 프로젝트: ${ctx.projectName} | 버전: ${ctx.projectVersion}`,
    `> ${ctx.tree.meta.derivedAt.slice(0, 10)} — DO NOT EDIT MANUALLY`,
    "",
  ].join("\n");
}

function buttonDoc(ctx: Ctx): string {
  const { tree } = ctx;
  return [
    header("Button", ctx),
    "## 1. 정의",
    "",
    "사용자가 클릭으로 액션을 트리거하는 기본 컨트롤.",
    "",
    "## 2. Anatomy",
    "",
    "| 슬롯 | 필수 | 설명 |",
    "|---|---|---|",
    "| `root` | ✓ | 버튼 컨테이너 |",
    "| `label` | ✓ | 텍스트 |",
    "| `iconLeading` | – | 좌측 아이콘 |",
    "| `iconTrailing` | – | 우측 아이콘 |",
    "| `spinner` | – | loading 상태 표시 |",
    "",
    "## 3. Variants",
    "",
    "| 차원 | 옵션 |",
    "|---|---|",
    "| variant | `primary` \\| `default` \\| `dashed` \\| `danger` |",
    "| size | `sm` \\| `md` \\| `lg` |",
    "| state | `default` \\| `hover` \\| `active` \\| `disabled` \\| `loading` \\| `focus` |",
    "",
    "## 4. 토큰 매핑 — variant: primary, size: md",
    "",
    "| 속성 | 토큰 | 값 |",
    "|---|---|---|",
    "| background | `{alias.action.primary}` | " + tree.alias.action.primary + " |",
    "| color | `{alias.text.onPrimary}` | " + tree.alias.text.onPrimary + " |",
    "| height | `{alias.control.height.md}` | " + tree.alias.control.height.md + "px |",
    "| paddingX | `{alias.control.padding.x}` | " + tree.alias.control.padding.x + "px |",
    "| borderRadius | `{map.radius.md}` | " + tree.map.radius.md + "px |",
    "| fontSize | `{map.font.size.sm}` | " + tree.map.font.size.sm + "px |",
    "| focus.ringWidth | `{alias.focus.ring.width}` | " + tree.alias.focus.ring.width + "px |",
    "| focus.ringColor | `{alias.focus.ring.color}` | " + tree.alias.focus.ring.color + " |",
    "",
    "## 5. 사용 케이스 (Do)",
    "",
    "- 한 화면의 주요 액션 1개에 `primary` 사용",
    "- 위험 액션(삭제·취소불가)에 `danger`",
    "- 보조 액션에 `default` 또는 `dashed`",
    "",
    "## 6. 사용 금지 (Don't)",
    "",
    "- 한 화면에 `primary` 버튼 여러 개",
    "- 라벨 6단어 초과",
    "- 아이콘만 있는 버튼 (별도 `IconButton` 컴포넌트 사용)",
    "",
    "## 7. 접근성",
    "",
    "- WCAG 2.2 AA: 라벨 4.5:1 대비, 포커스 링 2px·3:1 대비",
    "- KWCAG 2.2: 키보드만으로 트리거 가능, 한국어 라벨 존대 일관성",
    "- 터치 타겟: 모바일 최소 44×44px",
    "- `prefers-reduced-motion`: hover transition 0ms",
    "- 상태 전달: `disabled`·`loading`은 색뿐 아니라 아이콘·텍스트로도 표시",
    "",
    "## 8. 코드 예시 (HTML)",
    "",
    "```html",
    "<button class=\"xds-button xds-button--primary xds-button--md\">저장</button>",
    "```",
    "",
    "## 9. 관련 컴포넌트",
    "",
    "- IconButton — 아이콘 전용",
    "- ButtonGroup — 버튼 묶음",
    "- Link — 텍스트 네비게이션",
    "",
  ].join("\n");
}

function inputDoc(ctx: Ctx): string {
  const { tree } = ctx;
  return [
    header("Input", ctx),
    "## 1. 정의",
    "",
    "텍스트·숫자 등 단일 줄 입력을 위한 폼 컨트롤.",
    "",
    "## 2. Anatomy",
    "",
    "| 슬롯 | 필수 | 설명 |",
    "|---|---|---|",
    "| `root` | ✓ | 입력 컨테이너 |",
    "| `input` | ✓ | <input> 요소 |",
    "| `iconLeading` | – | 좌측 장식·검색 아이콘 |",
    "| `clear` | – | 입력 지우기 버튼 |",
    "",
    "## 3. Variants",
    "",
    "| 차원 | 옵션 |",
    "|---|---|",
    "| size | `sm` \\| `md` \\| `lg` |",
    "| state | `default` \\| `focus` \\| `disabled` \\| `error` |",
    "",
    "## 4. 토큰 매핑 — size: md",
    "",
    "| 속성 | 토큰 | 값 |",
    "|---|---|---|",
    "| background | `{alias.surface.elevated}` | " + tree.alias.surface.elevated + " |",
    "| color | `{alias.text.body}` | " + tree.alias.text.body + " |",
    "| border | `{alias.border.default}` | " + tree.alias.border.default + " |",
    "| height | `{alias.control.height.md}` | " + tree.alias.control.height.md + "px |",
    "| paddingX | `{alias.control.padding.x}` | " + tree.alias.control.padding.x + "px |",
    "| borderRadius | `{map.radius.md}` | " + tree.map.radius.md + "px |",
    "",
    "## 5. 사용 케이스 (Do)",
    "",
    "- 단일 줄 텍스트·숫자 입력",
    "- placeholder는 *예시*에만 사용 (라벨 대체 금지)",
    "",
    "## 6. 사용 금지 (Don't)",
    "",
    "- 여러 줄은 `Textarea`",
    "- 선택지 중 하나는 `Select`",
    "- 라벨 없이 placeholder만 (KWCAG 4.1 위반)",
    "",
    "## 7. 접근성",
    "",
    "- `<label>` 또는 `aria-label` 필수 (U-03)",
    "- 오류 메시지는 `aria-describedby`로 연결 (U-05)",
    "- 한국어 입력 형식 사전 안내 (U-13)",
    "- 포커스 링 가시성 2px+ (O-03)",
    "",
    "## 8. 코드 예시",
    "",
    "```html",
    "<label>",
    "  <span>이메일</span>",
    "  <input type=\"email\" class=\"xds-input\" />",
    "</label>",
    "```",
    "",
    "## 9. 관련 컴포넌트",
    "",
    "- Textarea — 여러 줄 입력",
    "- Select — 선택지 입력",
    "- SearchField — 검색 전용",
    "",
  ].join("\n");
}

function cardDoc(ctx: Ctx): string {
  const { tree } = ctx;
  return [
    header("Card", ctx),
    "## 1. 정의",
    "",
    "관련 정보를 시각적으로 묶어 표시하는 컨테이너.",
    "",
    "## 2. Anatomy",
    "",
    "| 슬롯 | 필수 | 설명 |",
    "|---|---|---|",
    "| `root` | ✓ | 카드 컨테이너 |",
    "| `header` | – | 제목·메타 영역 |",
    "| `body` | ✓ | 본문 영역 |",
    "| `footer` | – | 액션·메타 영역 |",
    "",
    "## 3. Variants",
    "",
    "| 차원 | 옵션 |",
    "|---|---|",
    "| elevation | `flat` \\| `raised` \\| `interactive` |",
    "",
    "## 4. 토큰 매핑 — elevation: raised",
    "",
    "| 속성 | 토큰 | 값 |",
    "|---|---|---|",
    "| background | `{alias.surface.elevated}` | " + tree.alias.surface.elevated + " |",
    "| border | `{alias.border.subtle}` | " + tree.alias.border.subtle + " |",
    "| borderRadius | `{map.radius.lg}` | " + tree.map.radius.lg + "px |",
    "| padding | `{map.size.md}` | " + tree.map.size.md + "px |",
    "",
    "## 5. 사용 케이스 (Do)",
    "",
    "- 진도·통계 등 단위 정보 묶음",
    "- 리스트 항목으로 사용 시 `interactive` elevation",
    "",
    "## 6. 사용 금지 (Don't)",
    "",
    "- 한 화면에 5개 이상의 raised card (시각 혼란)",
    "- card 안에 또 다른 card 중첩",
    "",
    "## 7. 접근성",
    "",
    "- `interactive` 변형은 키보드 포커스 가능 (`tabindex=\"0\"` + role)",
    "- 헤더가 있으면 의미 단위 구분 (`<h3>` 등)",
    "",
    "## 8. 코드 예시",
    "",
    "```html",
    "<article class=\"xds-card xds-card--raised\">",
    "  <h3>강의 시청 완료</h3>",
    "  <p>총 8개 중 6개를 완료했어요.</p>",
    "</article>",
    "```",
    "",
    "## 9. 관련 컴포넌트",
    "",
    "- ListItem — 단일 행",
    "- Panel — 더 큰 콘텐츠 영역",
    "",
  ].join("\n");
}

function modalDoc(ctx: Ctx): string {
  const { tree } = ctx;
  return [
    header("Modal", ctx),
    "## 1. 정의",
    "",
    "현재 화면을 차단하고 사용자의 명시적 결정을 요구하는 다이얼로그.",
    "",
    "## 2. Anatomy",
    "",
    "| 슬롯 | 필수 | 설명 |",
    "|---|---|---|",
    "| `overlay` | ✓ | 배경 오버레이 |",
    "| `dialog` | ✓ | 다이얼로그 본체 |",
    "| `title` | ✓ | 모달 제목 |",
    "| `body` | ✓ | 본문 메시지 |",
    "| `actions` | ✓ | 액션 버튼 (취소·확인) |",
    "| `close` | – | X 닫기 버튼 |",
    "",
    "## 3. Variants",
    "",
    "| 차원 | 옵션 |",
    "|---|---|",
    "| size | `sm` \\| `md` \\| `lg` |",
    "| tone | `default` \\| `danger` |",
    "",
    "## 4. 토큰 매핑",
    "",
    "| 속성 | 토큰 | 값 |",
    "|---|---|---|",
    "| dialog.background | `{alias.surface.elevated}` | " + tree.alias.surface.elevated + " |",
    "| dialog.border | `{alias.border.subtle}` | " + tree.alias.border.subtle + " |",
    "| dialog.borderRadius | `{map.radius.lg}` | " + tree.map.radius.lg + "px |",
    "| dialog.padding | `{map.size.lg}` | " + tree.map.size.lg + "px |",
    "",
    "## 5. 사용 케이스 (Do)",
    "",
    "- 되돌릴 수 없는 결정 (삭제 확인)",
    "- 사용자 입력이 반드시 필요한 폼",
    "",
    "## 6. 사용 금지 (Don't)",
    "",
    "- 단순 알림 (`Toast` 사용)",
    "- 폼 길이가 한 화면을 넘어가면 별도 페이지",
    "",
    "## 7. 접근성",
    "",
    "- `role=\"dialog\"` + `aria-modal=\"true\"` + `aria-labelledby` 필수",
    "- 열린 동안 포커스 트랩 (O-02 위반 회피)",
    "- ESC로 닫기 + 닫힘 후 트리거에 포커스 복귀",
    "- 키보드 트랩 점검 (O-02)",
    "- 다크모드: surface.elevated가 다크에서 충분히 구분되는지 확인",
    "",
    "## 8. 코드 예시",
    "",
    "```html",
    "<div role=\"dialog\" aria-modal=\"true\" aria-labelledby=\"m1\">",
    "  <h2 id=\"m1\">변경 사항을 저장할까요?</h2>",
    "  <p>토큰 변경 사항이 자동 저장됩니다.</p>",
    "  <button>취소</button>",
    "  <button class=\"xds-button--primary\">확인</button>",
    "</div>",
    "```",
    "",
    "## 9. 관련 컴포넌트",
    "",
    "- Drawer — 측면 슬라이드",
    "- Popover — 비차단 부가 정보",
    "- Toast — 일시 알림",
    "",
  ].join("\n");
}

function formDoc(ctx: Ctx): string {
  return [
    header("Form", ctx),
    "## 1. 정의",
    "",
    "여러 입력 필드를 묶어 사용자 데이터를 수집하는 컨테이너.",
    "",
    "## 2. Anatomy",
    "",
    "| 슬롯 | 필수 | 설명 |",
    "|---|---|---|",
    "| `root` | ✓ | <form> 요소 |",
    "| `field` | ✓ | label + input + helper 묶음 |",
    "| `actions` | ✓ | 제출·취소 버튼 그룹 |",
    "",
    "## 3. Variants",
    "",
    "| 차원 | 옵션 |",
    "|---|---|",
    "| layout | `vertical` \\| `horizontal` |",
    "| size | `compact` \\| `default` \\| `comfortable` |",
    "",
    "## 4. 토큰 매핑",
    "",
    "Form은 Input·Button·Label 컴포넌트의 조합으로, 별도 페어 토큰을 갖지 않는다.",
    "각 자식 컴포넌트의 토큰 매핑을 참조한다.",
    "",
    "## 5. 사용 케이스 (Do)",
    "",
    "- 필드 그룹화 (관련 입력 묶음)",
    "- 명시적 제출 액션 (Enter 키 + Submit 버튼 모두 지원)",
    "",
    "## 6. 사용 금지 (Don't)",
    "",
    "- 포커스 이동만으로 자동 제출 (U-12 위반)",
    "- 라벨 없는 placeholder만 의존 (U-04 위반)",
    "",
    "## 7. 접근성",
    "",
    "- 모든 필드에 명시적 `<label>` (U-03)",
    "- 오류 메시지는 필드 직후 + `aria-describedby` (U-05)",
    "- 입력 형식 사전 안내 (U-13)",
    "- 제출 실패 시 첫 오류 필드로 포커스 자동 이동",
    "- 한국어 어법·존대 일관 (K-05)",
    "",
    "## 8. 코드 예시",
    "",
    "```html",
    "<form class=\"xds-form\">",
    "  <label>",
    "    <span>이름</span>",
    "    <input class=\"xds-input\" />",
    "  </label>",
    "  <button class=\"xds-button--primary\">저장</button>",
    "</form>",
    "```",
    "",
    "## 9. 관련 컴포넌트",
    "",
    "- Input · Select · Textarea · Checkbox · Radio",
    "- FormField — 라벨·입력·헬퍼 묶음",
    "",
  ].join("\n");
}
